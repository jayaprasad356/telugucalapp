package com.vibame.telugupanchangamcalendar.model;

public class OldData {
    private String imgGod;
    private String tvTitle;

    public OldData(String imgGod, String tvTitle) {
        this.imgGod = imgGod;
        this.tvTitle = tvTitle;
    }

    public String getImgGod() { return imgGod; }

    public void setImgGod(String imgGod) { this.imgGod = imgGod; }

    public String getTvTitle() { return tvTitle; }

    public void setTvTitle(String tvTitle) { this.tvTitle = tvTitle; }
}
